/**
 * User Activity Tracker Collection
 * --------------------------------
 * Purpose : Track user activities (task/session based)
 * Used by : APP / ADMIN / REPORTING
 *
 * Contains:
 * - User identity
 * - Activity name & classification
 * - Status tracking (enum)
 * - Start & end time
 */

import { Prop, Schema, SchemaFactory } from '@nestjs/mongoose';
import { HydratedDocument } from 'mongoose';
import { AutoGenerated } from 'src/core/decorators/auto-generated.decorator';
import { ActivityStatus } from 'src/shared/enums/activity.enums';

export type ActivityDocument = HydratedDocument<Activity>;

@Schema({ timestamps: true})
export class Activity {
  /* ======================================================
   * IDENTITY
   * ====================================================== */

  // Unique activity reference ID
  @AutoGenerated()
  @Prop({ required: true, unique: true, type: String })
  activityId: string;

  // User who performed the activity
  @Prop({ required: true, type: String })
  workSessionId: string;

  // User who performed the activity
  @Prop({ required: true, type: String })
  userId: string;

  // User name (for quick reference)
  @Prop( { type: String})
  userName: string;

  // User who performed the activity
  @Prop({ required: true, type: String })
  vanId: string;

  // User name (for quick reference)
  @Prop( { type: String})
  vanName: string;

  /* ======================================================
   * ACTIVITY DETAILS
   * ====================================================== */

  // Activity name (e.g., "Meeting", "Development", "Break")
  @Prop({ required: true, trim: true, type: String })
  name: string;

  // Description of activity
  @Prop({ trim: true, type: String })
  description: string;

  // Activity status
  @Prop({
    type: String,
    enum: ActivityStatus,
    default: ActivityStatus.ACTIVE,
  })
  status: ActivityStatus;

  // Category (e.g., WORK, PERSONAL)
  @Prop({ trim: true, type: String})
  category: string;

  // Sub-category (e.g., CODING, TESTING)
  @Prop({ trim: true, type: String })
  subCategory: string;

  /* ======================================================
   * TIME TRACKING
   * ====================================================== */

  // Activity start time
  @Prop({ required: true, type: Date })
  startTime: Date;

  // Activity end times
  @Prop({ type: Date})
  endTime: Date;
}

export const ActivitySchema = SchemaFactory.createForClass(Activity);
