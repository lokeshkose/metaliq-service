/**
 * Inventory Collection
 * --------------------
 * Purpose : Product stock management
 * Used by : BACK_OFFICE / ADMIN / SALES
 *
 * Contains:
 * - Inventory identity
 * - Product reference
 * - Stock quantities
 * - Settlement date
 * - Status
 */

import { Prop, Schema, SchemaFactory } from '@nestjs/mongoose';
import { HydratedDocument } from 'mongoose';
import { AutoGenerated } from 'src/core/decorators/auto-generated.decorator';
import { VanInventoryStatus } from 'src/shared/enums/van-inventory.enums';

export type VanInventoryDocument = HydratedDocument<VanInventory>;

/* ======================================================
 * INVENTORY SCHEMA
 * ====================================================== */

@Schema({ timestamps: true, collection: 'inventories' })
export class VanInventory {
  /* ======================================================
   * IDENTITY
   * ====================================================== */

  @AutoGenerated()
  @Prop({ required: true, unique: true, index: true, type: String })
  inventoryId: string;

  /* ======================================================
   * PRODUCT REFERENCE
   * ====================================================== */

  @Prop({ required: true, index: true, type: String })
  productId: string;

  /* ======================================================
   * VAN REFERENCE
   * ====================================================== */
  @Prop({ required: true, index: true, type: String })
  vanId: string;

  /* ======================================================
   * QUANTITY DETAILS
   * ====================================================== */

  // Total stock quantity in pieces
  @Prop({
    required: true,
    min: 1,
    type: Number,
  })
  quantity: number;

  // Reserved quantity for orders (optional)
  @Prop({
    min: 1,
    default: 0,
    type: Number,
  })
  reservedQuantity?: number;

  /* ======================================================
   * SETTLEMENT
   * ====================================================== */

  // ISO settlement date (optional)
  @Prop({ type: Date })
  settlementDate?: Date;

  /* ======================================================
   * STATUS
   * ====================================================== */

  @Prop({
    type: String,
    enum: VanInventoryStatus,
    default: VanInventoryStatus.ACTIVE,
    index: true,
  })
  status: VanInventoryStatus;
}

export const VanInventorySchema = SchemaFactory.createForClass(VanInventory);
